#Estamos atribuindo o valor da string 'Erlich Bachman' à variável name.
#Estamos atribuindo o valor do número 16180339887 à variável user_id.
#Estamos atribuindo o valor do número 0.75 à variável progress.
#Estamos atribuindo o valor do número 60 à variável exp.
#Estamos atribuindo o valor verdade True à variável verified.

# Também podemos alterar o valor de uma variável ou imprimi-la:
xp = 70
xp = 80

