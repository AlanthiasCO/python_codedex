# Tipos de dados: Inteiros, Floats, Strings e Booleanos

# Inteiros: 
# Números inteiros são números sem parte decimal. Eles podem ser positivos ou negativos. 
# Exemplos de inteiros são -14, 0, 42 e 100.
year = 2023
age = 32

# Floats:
# Números de ponto flutuante são números que possuem uma parte decimal.
# Exemplos de floats são -1.5, 0.0, 3.14 e 2.71828.
pi = 3.14159
meal_cost = 12.99

#Strings:
# Strings são sequências de caracteres. Elas são usadas para representar texto. 
# Strings são delimitadas por aspas simples (' ') ou aspas duplas (" ").
# Exemplos de strings são "Hello, World!", 'Python' e '42'.
name = 'Richard Hendricks'
message = "good nite"
username = '@snoopdogg'

#Booleanos:
# Booleanos são um tipo de dado que pode ser True ou False.
# Eles são usados para representar valores lógicos.
# Exemplos de booleanos são True e False.
late_to_class = False
cranky = True
